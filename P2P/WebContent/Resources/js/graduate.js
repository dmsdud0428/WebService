
function popup_open(i) {
	if(i==2016)
		window.open("edu2016.jsp", "a", "width=1000, height=750, left=100, top=50");
	else if(i==2017)
		window.open("edu2017.jsp", "a", "width=1000, height=750, left=100, top=50");
	else if(i==2018)
		window.open("edu2018.jsp", "a", "width=1000, height=750, left=100, top=50");
	else
		window.open("edu2016.jsp", "a", "width=1000, height=750, left=100, top=50");
}


